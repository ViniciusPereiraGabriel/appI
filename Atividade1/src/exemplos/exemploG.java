
package exemplos;

public class exemploG {
    public static void main(String[] args) {
        int num = 0;
        System.out.println("valor original:" + num);
        num += 3;
        System.out.println("valor de num:" + num);
        num *= 4;
        System.out.println("valor de num:" + num);
        num /= 2;
        System.out.println("valor de num:" + num);
        num %= 2;
        System.out.println("valor de num:" + num);
    }
}
