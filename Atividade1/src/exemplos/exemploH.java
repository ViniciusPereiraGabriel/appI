package exemplos;

public class exemploH {

    public static void main(String[] args) {

        int numero = 25;
        System.out.println(numero);

        numero++;
        System.out.println(numero);

        numero++;
        System.out.println(numero);

        numero--;
        System.out.println(numero);

        numero += 5;
        System.out.println(numero);

        numero -= 2;
        System.out.println(numero);
    }
}
